

//Programa para saber cuantas pulgadas son 6 centimetros cm

public class Declaraciones_Operadores2 {

	public static void main(String[] args) {
		
		final double apulgadas=2.54;
		
		double cm=6;
		
		double resultado=cm/apulgadas;
		
		
		System.out.println("En " + cm + "cm hay " + resultado + " pulgadas");

	}

}

/* Nota: en java puedes declarar en una misma linea varias variables o constantes
 * que pertenezcan al mismo tipo de dato 
 * ejemplo:
 * int operador1,operador2, resultado;
 * operador1=8;
 * operador2=7;
 * resultado=operador1+operador2*/